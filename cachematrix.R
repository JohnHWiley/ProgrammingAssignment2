## Functions makeCacheMatrix and cacheSolve calculate and cache matrix inverse
## makeCacheMatrix is custom matrix object that can cache its own inverse
## cacheSolve function returns or caches matrix inverse
## Assumes that matrix is invertible

## creates list of available functions for caching matrix

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinv <- function(solve) m <<- solve
    getinv <- function() m
    list(set = set, get = get,
         setinv = setinv,
         getinv = getinv)
}


## Function returns matrix inverse if it exists
## will cache matrix if it does not

cacheSolve <- function(x, ...) {
    ## Return a matrix that is the inverse of 'x'
    m <- x$getinv()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinv(m)
    m
}
